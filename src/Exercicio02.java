public class Exercicio02 {
    public static void main(String[] args) {
        //tabuada do 1 ao 5

        int numTabuada = 1;

        for(int temp = 1; temp <= 5; temp++){


            numTabuada++;

            int tabuada = numTabuada * temp;
            System.out.println(tabuada);

        }
    }
}
